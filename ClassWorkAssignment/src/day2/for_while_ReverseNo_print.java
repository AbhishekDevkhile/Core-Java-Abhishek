package day2;

public class for_while_ReverseNo_print {

	public static void main(String[] args) {

		System.out.println("Print Reverse no using for loop");
		
		for(int i=300;i>=175;i--)
		{
			System.out.println(" " +i);
		}
		
		
		
	System.out.println();
	System.out.println();
	
	System.out.println("Print Reverse no. using While loop");
	
	int j=300;
		while(j>=175)
		{
			System.out.println(" " +j);
			j--;
		}
		
	}

}
