package day2;

public class for_while_Print_20_nos {

	public static void main(String[] args) {
		
		System.out.println("Print 1 to 20 nos using for loop.");
		
		for(int i=1;i<=20;i++)
		{
			System.out.println(" "+i);
		}
		
	
		System.out.println("");
		System.out.println("");
		
		
		System.out.println("Print 1 to 20 nos using while loop.");
		int j=1;
		while(j<=20)
		{
			System.out.println(" " +j);
			j++;
		}

	}

}
